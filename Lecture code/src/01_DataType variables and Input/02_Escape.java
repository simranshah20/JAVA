class Escape {
    public static void main(String[] args) {
        System.out.println("Hello Everyone....");   //normal
        System.out.println("Hello 'Everyone'....");   //to show everyone in single quote,use '' or \'
        System.out.println("Hello \"Everyone\"....");   //to show everyone in double quote,use \"
        System.out.println("How \\are\\ you?");  //to show backslash,use \\
        System.out.println("Hello everyone \nHow are you");   //to show in nextline or newline,use \n
        System.out.println("I am good.\tWhat about you?");   //to show 1 tab space,use \t
        System.out.println("I am good.\b What about you?");   //to show 1 space back,use \b
    }
}
